    /**
     * Inicia sesion en el servidor de chat.
     */
    private void login() {
        String username = txtUsername.getText();
        
        if (username == null || (username = username.trim()).isEmpty()) {
            showErrorDialog("Debe de ingresar un nombre de usuario.");
            return;
        }
        
        String address = txtAddress.getText();
        
        if (address == null || (address = address.trim()).isEmpty()) {
            showErrorDialog("Debe de ingresar la direcci贸n del servidor.");
            return;
        }
        
        try {
            ChatClientWindow client = new ChatClientWindow(username, address);
            client.setVisible(true);
            dispose();
        } catch (ChatException e) {
            JOptionPane.showMessageDialog(this, e.getMessage(), WINDOW_TITLE, JOptionPane.ERROR_MESSAGE);
        }
    }
    
    /**
     * Muestra un mensaje de error.
     * @param message El mensaje a mostrar.
     */
    private void showErrorDialog(String message) {
        JOptionPane.showMessageDialog(this, message, WINDOW_TITLE, JOptionPane.ERROR_MESSAGE);
    }

